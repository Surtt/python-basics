expenses = [20, 35, 15, 40, 10, 28, 50]
sum_expenses = sum(expenses)
average_expense = sum_expenses / len(expenses)
min_expense = min(expenses)
max_expense = max(expenses)
print(f"Total Expenses: ${sum_expenses}")
print(f"Average Expense: ${average_expense:.2f}")
print(f"Minimum Expense: ${min_expense}")
print(f"Maximum Expense: ${max_expense}")
